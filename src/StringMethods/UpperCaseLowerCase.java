package StringMethods;
//strUpLow.toUpperCase() Prints the entered string to Uppercase
//toLowerCase() prints the entered string to Lowercase
public class UpperCaseLowerCase {
    public static void main(String[] args) {
        String strUpLow = "Donald Trump";
        System.out.println("Upper case of the entered string is: "+strUpLow.toUpperCase());
        System.out.println("Lower case of the entered string is: "+strUpLow.toLowerCase());
    }
}
