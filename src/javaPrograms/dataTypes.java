package javaPrograms;

public class dataTypes {
    public static void main(String[] args) {

        byte myBy = 123;
        System.out.println("Integer value is: " + myBy);

        short myShort = 2500;
        System.out.println("Integer value is: " + myShort);

        int num = 5;
        System.out.println("Integer value is: " + num);

        char myLetter = 'a';
        System.out.println("Char value is: " + myLetter);

        float myFloat = 5.889f;
        System.out.println("Float value is: " + myFloat);

        boolean myValue = true;
        System.out.println("boolean value is: " + myValue);

    }

}
