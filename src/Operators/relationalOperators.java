package Operators;

public class relationalOperators {
    public static void main(String[] args) {

        //Initialising the value
        int a = 20;
        int b = 30;

        //Printing the value
        System.out.println("Value for a is: "+a);
        System.out.println("Value for b is: "+b);

        //Checking the relational operator contions and printing the value
        //Checking == value
        System.out.println("Equal value is: "+(a==b));

        //Checking != value
        System.out.println("Not eqval value is: "+(a!=b));

        //Checking <= value
        System.out.println("Less than and equal value is: "+(a<=b));

        //Checking >= value
        System.out.println("Greater than or Equal value is: "+(a>=b));

        //Checking > value
        System.out.println("Greater than value is: "+(a>b));

        //Checking < value
        System.out.println("Less than value is: "+(a<b));

    }
}
