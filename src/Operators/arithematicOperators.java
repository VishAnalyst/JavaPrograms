package Operators;

public class arithematicOperators {
    public static void main (String[] args){

        //Initialising the values
        int a = 100;
        int b = 50;

        //Adding and printing the value
        System.out.println("The value for addition is: " + (a+b));

        //Subtracting and printing the value
        System.out.println("The value for Subtracting is: " + (a-b));

        //Multiplicating and printing the value
        System.out.println("The value for Multiplication is: " + (a*b));

        //Divition and printing the value
        System.out.println("The value for Division is: " + (a/b));

        //And and printing the value
        System.out.println("The value for modulo is: " + (a&b));


    }

}
